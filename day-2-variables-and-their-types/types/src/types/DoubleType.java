package types;

public class DoubleType {

	public static void main(String[] args) {
        double varDouble = 2.356;
        double doubleRecebendoFloat = 1.409F;
        double doubleRecebendoLong = 12930L;
        System.out.println(varDouble);
        System.out.println(doubleRecebendoFloat);
        System.out.println(doubleRecebendoLong);

	}

}
